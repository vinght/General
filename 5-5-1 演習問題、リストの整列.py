#演習問題 1
'''
「リストを昇順にソートする」
右のコードエリアには、複数のリンゴの重さがリストに格納されています。
このリストの要素を軽い順に整列して、配列として出力してください。

プログラムを実行して、正しく出力されれば演習課題クリアです！

期待する出力値
[288, 292, 300, 310, 322, 346]
'''
#リストを昇順にソートする

apples = [310, 322, 292, 288, 300, 346]
# ここに、要素をソートして表示するコードを記述する
print(sorted(apples))



#演習課題「数値を5から15まで表示する」
# whileによるループ処理
i = 5
while i <= 15:
    print(i)
    i += 1


# 演習課題「数値を5から1までカウントダウン表示する」
i = 1
while i > 0:
    print(i)
    i -= 1


# 演習課題「1から10までの[偶数]を表示する」
